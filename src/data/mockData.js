// Generate 12-week trajectory data for SKU deep dive charts
export function generateTrajectoryData(sku, scenario = 'balanced') {
  const scenarios = {
    conservative: { safetyMultiplier: 1.4, cycleMultiplier: 1.3 },
    balanced:     { safetyMultiplier: 1.18, cycleMultiplier: 1.15 },
    aggressive:   { safetyMultiplier: 0.95, cycleMultiplier: 1.05 },
  }
  const s = scenarios[scenario]
  const weeklyDemand = sku.forecastMean
  const startStock = sku.currentDOH * weeklyDemand

  return Array.from({ length: 13 }, (_, i) => {
    const currentPolicy = Math.max(0, startStock - weeklyDemand * i * 0.9 + (i % 4 === 0 ? weeklyDemand * 3 : 0))
    const recommended = Math.max(
      sku.recommendedSafetyStock * s.safetyMultiplier,
      startStock * 1.4 - weeklyDemand * i * 0.85 + (i % 4 === 0 ? weeklyDemand * 3.5 : 0)
    )
    const riskBoundary = sku.recommendedSafetyStock * s.safetyMultiplier * 0.6
    return {
      week: `W${i + 1}`,
      currentPolicy: Math.round(currentPolicy),
      recommended: Math.round(recommended),
      riskBoundary: Math.round(riskBoundary),
    }
  })
}

export function generateServiceLevelDistribution(sku) {
  const currentSL = sku.currentServiceLevel
  const targetSL = sku.targetServiceLevel
  return Array.from({ length: 20 }, (_, i) => {
    const sl = 80 + i * 1.1
    const currentProb  = Math.exp(-0.5 * Math.pow((sl - currentSL) / 3.5, 2)) * 25
    const optimizedProb = Math.exp(-0.5 * Math.pow((sl - targetSL) / 1.8, 2)) * 35
    return {
      sl: sl.toFixed(1) + '%',
      current:   Math.max(0, Math.round(currentProb * 10) / 10),
      optimized: Math.max(0, Math.round(optimizedProb * 10) / 10),
    }
  })
}

// 26-week demand time series
export function generateDemandTimeSeries(sku) {
  const base = sku.forecastMean
  const cv = sku.demandCV
  const trend = sku.forecastTrend === 'up' ? 0.015 : sku.forecastTrend === 'down' ? -0.01 : 0
  return Array.from({ length: 26 }, (_, i) => {
    const noise = 1 + (Math.sin(i * 1.3 + sku.id.charCodeAt(4)) * cv * 0.8)
    const trendFactor = 1 + trend * i
    const seasonal = 1 + 0.12 * Math.sin((i / 26) * 2 * Math.PI)
    return {
      week: `W${i + 1}`,
      actual:     Math.round(base * noise * trendFactor * seasonal),
      forecast:   Math.round(base * trendFactor * seasonal),
      upperBound: Math.round(base * trendFactor * seasonal * (1 + cv)),
      lowerBound: Math.max(0, Math.round(base * trendFactor * seasonal * (1 - cv))),
    }
  })
}

